    <h1>Resumen sobre Java</h1>
    <p>es un lenguaje de programación utilizado para crear páginas web interactivas y dinámicas.
    Permitiendo funciones como animaciones, actualizaciones de contenido y formularios interactivos sin necesidad de recargar la página.</p>
<h2>Sintaxis de JAVA</h2>
 <p>Define las reglas para escribir código válido(solo se puede declarar por "var","let" o "const"). 
    Utilizando sentencias que generalmente terminan en punto y coma";" y bloques de código delimitaciones por llaves {}.</p>
    <h2>Estructuras Repetitivas de JAVA</h2>
    <p>Permiten ejecutar un bloque de codigo muchas veces. Las principales son ("for", "while" y "do-while")</p>
    <table>
        <tr>
            <th>for</th>
            <td>Se usa para saber cuantas veces se repitiran</td>
            <th>while</th>
            <td>Ejecuta el codigo mientra una condicion sea verdadero</td>
            <th>do-while</th>
            <td>Ejecuta el cuerpo del bucle una vez</td>
        </tr>
    </table>
    <h2>POO (Atributos y  Metodos)</h2>
    <p>Los atributos son los datos que describen las características de un objeto
    mientras que los métodos son las funciones que definen su comportamiento y las acciones que puede realizar.</p>
    <h2>Herencia de JAVA</h2>
    <p>Permite que una clase (la hija o subclase) herede propiedades y métodos de otra clase existente (la padre o superclase). </p>
    
